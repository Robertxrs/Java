    private void bttnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnCadastrarActionPerformed
        String clienteId = txtClienteId.getText();
        String dataPagamentoString = txtDataPagamento.getText();
        String veiculoId = txtVeiculoId.getText();
        String tipo = (String)comboxTipo.getSelectedItem();
        String status = (String)comboxStatus.getSelectedItem();
        
           try {
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        Date dataPagamento = dateFormat.parse(dataPagamentoString);
        

        ClienteDAO clienteDAO = new ClienteDAO();
        VeiculoDAO veiculoDAO = new VeiculoDAO();


        Clientes cliente = clienteDAO.findById(Integer.parseInt(clienteId));
        Veiculos veiculo = veiculoDAO.findById(Integer.parseInt(veiculoId));
        
        Pagamentos novoPagamento = new Pagamentos();
        novoPagamento.setCliente_id(cliente);
        novoPagamento.setData_pagamento(dataPagamento);
        novoPagamento.setVeiculo_id(veiculo);
        novoPagamento.setTipo(tipo);
        novoPagamento.setStatus(status);
        
        PagamentoDAO pagamentoDAO = new PagamentoDAO();
        pagamentoDAO.cadastrar(novoPagamento); 
        JOptionPane.showMessageDialog(this, "Cadastro realizado com sucesso.");
       
        this.dispose();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Dados incorretos.", "Aviso", JOptionPane.WARNING_MESSAGE);
    }




    }//GEN-LAST:event_bttnCadastrarActionPerformed

