    private void bttnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnCadastrarActionPerformed
      String nome = txtNome.getText();
      String endereco = txtEndereco.getText();
      String cidade = txtCidade.getText();
      String estado = txtEstado.getText();
      
     try{
      Locais novoLocal = new Locais();
      novoLocal.setNome(nome);
      novoLocal.setEndereco(endereco);
      novoLocal.setCidade(cidade);
      novoLocal.setEstado(estado);
      LocaisDAO dao = new LocaisDAO();
      dao.cadastrar(novoLocal);
      
      JOptionPane.showMessageDialog(this, "Cadastro realizado!");
      
     }catch(Exception e){
         JOptionPane.showMessageDialog(this, "Ocorreu uma falha: \n" + e.getMessage());
     } 
      
 
        
    }//GEN-LAST:event_bttnCadastrarActionPerformed

