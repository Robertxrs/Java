    private boolean validarData(String data) {
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yy");
        sdf.setLenient(false);
        try {
            Date dataConsulta = sdf.parse(data);
            if (dataConsulta.before(new Date())) {
                JOptionPane.showMessageDialog(this, "A data da consulta não pode ser no passado.");
            }
            return true;
        } catch (ParseException e) {
            return false;
        }

    }

    private boolean validarNome(String nome) {
        return nome.matches("^[a-zA-Z ]+$");
    }

    private boolean validarFormatoCPF(String cpf) {
        return cpf.matches("\\d{11}");
    }

    private boolean validarFormatoTelefone(String telefone) {
        String apenasDigitos = telefone.replaceAll("[^\\d]", "");

        return apenasDigitos.matches("\\d{9}|\\d{11,12}");
    }

