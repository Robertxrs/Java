    private void jButtonCadasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadasActionPerformed
        try {
            String name = jTextFieldName.getText();
            String telefone = jTextFieldTel.getText();
            String cpf = jTextFieldCPF.getText();
            String dataConsulta = jTextFieldDate.getText();

            if (!validarNome(name)) {
                JOptionPane.showMessageDialog(this, "Nome inválido. Insira um nome válido.");
                return;
            }

            if (!validarFormatoTelefone(telefone)) {
                JOptionPane.showMessageDialog(this, "Telefone inválido. Insira um telefone válido.");
                return;
            }

            if (!validarFormatoCPF(cpf)) {
                JOptionPane.showMessageDialog(this, "CPF inválido. Insira um CPF válido.");
                return;
            }
            if (!validarData(dataConsulta)) {
                JOptionPane.showMessageDialog(this, "Data inválida. Insira uma data válida no formato dd/MM/aa.");
                return;
            }

            boolean ehPaciente = jCheckBoxPac.isSelected();
            Consulta novaConsulta = new Consulta(name, cpf, telefone, dataConsulta, ehPaciente);
            tabelaConsulta.incluirConsulta(novaConsulta);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "[ERRO] Digite valores numéricos válidos.");
        } finally {
            dispose();
        }
    }//GEN-LAST:event_jButtonCadasActionPerformed

